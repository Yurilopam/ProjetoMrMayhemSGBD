    private void botaoPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPesquisarActionPerformed
        DefaultTableModel dtmProdutos = (DefaultTableModel) jTable1.getModel();
        AluguelDAO aluguelDAO = new AluguelDAOImpl();
        List<Aluguel> alugueis = aluguelDAO.findAll();
        if (!alugueis.isEmpty()){
            for (Aluguel aluguel : alugueis){
                Object[] dados = {aluguel.getCodigoAluguel(), aluguel.getCliente().getCpf(), 
                    aluguel.getSituacao(), aluguel.getDataDevolucao()};
                dtmProdutos.addRow(dados);
            }
            botaoPesquisar.setEnabled(false);
            botaoImprimir.setEnabled(true);
        } else {
            JOptionPane.showMessageDialog(TelaVerificarAlugueis.this, "Não existem registros!");
        }
    }//GEN-LAST:event_botaoPesquisarActionPerformed

