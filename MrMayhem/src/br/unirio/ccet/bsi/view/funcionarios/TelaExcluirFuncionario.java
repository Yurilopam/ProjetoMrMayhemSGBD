    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        FuncionarioDAO funcionarioDAO = new FuncionarioDAOImpl();
        Funcionario funcionarioAExcluir;
        funcionarioAExcluir = funcionarioDAO.findByCtps(campoCtps.getText());
        funcionarioDAO.delete(funcionarioAExcluir);
        JOptionPane.showMessageDialog(TelaExcluirFuncionario.this, "O funcionário foi excluído com sucesso!");
        resetarCampos();
    }//GEN-LAST:event_botaoExcluirActionPerformed

