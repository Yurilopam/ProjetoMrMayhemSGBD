    private void botaoPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPesquisarActionPerformed
        FuncionarioDAO funcionarioDAO = new FuncionarioDAOImpl();
        Funcionario funcionario;
        funcionario = funcionarioDAO.findByCtps(campoCtps.getText());
        campoNome.setText(funcionario.getNome());
        campoDataNascimento.setText(new SimpleDateFormat("dd/MM/yyyy").format(funcionario.getDataNascimento()));
        campoNacionalidade.setText(funcionario.getNacionalidade());
        campoEmail.setText(funcionario.getEmail());
        campoCpf.setText(funcionario.getCpf());
        campoRg.setText(funcionario.getRg());
        campoCtps.setText(funcionario.getCtps());
        campoEstadoCivil.setSelectedIndex(recuperarEstadoCivil(funcionario));
        campoTelefone.setText(funcionario.getTelefone());
        campoRua.setText(funcionario.getRua());
        campoNumero.setText(funcionario.getNumeroRua());
        campoBairro.setText(funcionario.getBairro());
        campoCep.setText(funcionario.getCep());
        campoFuncao.setSelectedIndex(recuperarFuncao(funcionario));
        campoSalario.setText(funcionario.getSalario());
        campoDataAdmissao.setText(new SimpleDateFormat("dd/MM/yyyy").format(funcionario.getDataCadastramento()));
        botaoExcluir.setEnabled(true);
    }//GEN-LAST:event_botaoPesquisarActionPerformed
    
    private int recuperarEstadoCivil(Funcionario dadosFuncionario) {
        switch(dadosFuncionario.getEstadoCivil()){
            case "SOLTEIRO":
                return 0;
            case "CASADO":
                return 1;
            case "DIVORCIADO":
                return 2;
            case "VIUVO": 
                return 3;
            case "SEPARADO":
                return 4;
        } return -1;
    }
    
    private int recuperarFuncao(Funcionario dadosFuncionario) {
        switch(dadosFuncionario.getTipoFuncionario()){
            case "SUPERVISOR":
                return 0;
            case "ATENDENTE":
                return 1;
            case "GERENTE":
                return 2;
            case "ENTREGADOR": 
                return 3;
            case "ALFAIATE":
                return 4;
        } return -1;
    }   
    
