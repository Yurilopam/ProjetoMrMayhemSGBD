    private void botaoEnviarEmailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEnviarEmailActionPerformed
        if (formularioEnvioValidado()){
            DateFormat dateFormat = new SimpleDateFormat("yyyyMMddHHmmss");
            Date date = new Date();
            String nomeDoRelatorio = dateFormat.format(date);
            String nomeDoRelatorioPdf = nomeDoRelatorio+".pdf";
            Pdf pdf = new Pdf(jTable1, nomeDoRelatorioPdf);
            Mail mail = new Mail();
            try {
                campoEmailDestinatario.setEditable(false);
                campoAssunto.setEditable(false);
                campoMensagem.setEditable(false);
                botaoEnviarEmail.setEnabled(false);
                mail.enviarEmailComAnexo(Login.getIdUsuario()+"mrmayhem@gmail.com", "1wdvfe@3", campoEmailDestinatario.getText(), 
                        campoAssunto.getText(), campoMensagem.getText(), nomeDoRelatorioPdf);
            } catch (IOException ex) {
                Logger.getLogger(TelaPesquisaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }
            resetarCampos();
            JOptionPane.showMessageDialog(TelaEnvioRelatorio.this, "E-mail enviado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(TelaEnvioRelatorio.this, "Preencha todos os campos para enviar seu email.");
        }
    }//GEN-LAST:event_botaoEnviarEmailActionPerformed

