    private boolean formularioEnvioValidado() {
        return !StringUtils.isEmpty(campoEmailDestinatario.getText())
                && !StringUtils.isEmpty(campoAssunto.getText())
                && !StringUtils.isEmpty(campoMensagem.getText());
    }

    private void resetarCampos() {
        campoEmailDestinatario.setEditable(true);
        campoAssunto.setEditable(true);
        campoMensagem.setEditable(true);
        campoEmailDestinatario.setText(null);
        campoAssunto.setText(null);
        campoMensagem.setText(null);
        botaoEnviarEmail.setEnabled(true);
    }
