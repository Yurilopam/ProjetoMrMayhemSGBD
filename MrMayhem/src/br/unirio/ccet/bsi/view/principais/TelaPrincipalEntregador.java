    private boolean avisar = true;

    public TelaPrincipalEntregador() {
        initComponents();
        
        addWindowStateListener((WindowEvent arg0) -> {
            if (arg0.equals(WindowEvent.WINDOW_CLOSING)){
                avisar = false;
            }
        });
        
        Thread t = new Thread(){
            @Override
            public void run() {
                EntregaDAO entregaDAO = new EntregaDAOImpl();
                List<Entrega> entregas = entregaDAO.findAll();
                int contadorEntregasPendentes = 0;
                while(avisar){
                    for (Entrega entrega : entregas){
                        if (entrega.getStatus().equals((Enums.Status.PENDENTE).toString())){
                            contadorEntregasPendentes++;
                            avisar = true;
                        } else {
                            contadorEntregasPendentes = 0;
                            avisar = false;
                        }
                    }
                    if (contadorEntregasPendentes > 0)
                        JOptionPane.showMessageDialog(getParent(), "VocÃª possui entregas pendentes!");
                    try {
                        Thread.sleep(10 * 1000);
                    } catch (InterruptedException e) {
                        e.printStackTrace();
                    }
                }
            }
        };
        t.start();
        
    }

